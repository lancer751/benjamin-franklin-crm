import { Hono } from "hono";
import { logger } from "hono/logger";
import { cors } from "hono/cors";
import { HTTPException } from "hono/http-exception";
import type { ErrorResponse } from "shared";
import { apiRoutes } from "./routes/api.route";
import { prisma } from "@repo/database";
import { secureHeaders } from "hono/secure-headers";
import { rateLimiter } from "hono-rate-limiter";
import { csrf } from "hono/csrf";

export const app = new Hono();
const allowedOrigins = process.env.ALLOWED_ORIGINS?.split(",") ?? [];
const proofOrigins = process.env.ALLOWED_ORIGINS?.split(",") ?? [];
app.use("*", logger());
app.use(
  "*",
  cors({
    origin: allowedOrigins,
    credentials: true,
    allowMethods: ["GET", "POST", "PUT", "DELETE", "OPTIONS", "PATCH"],
    allowHeaders: ["Content-Type", "Authorization", "xxx-csrf-access-token"],
  }),
);


// app.use(csrf({ origin: allowedOrigins.concat(proofOrigins) }));
app.use(secureHeaders());
// Apply rate limiting middleware
app.use(
  rateLimiter({
    windowMs: 15 * 60 * 1000, // 15 minutes
    standardHeaders: true,
    limit: 200, // Limit each client to 100 requests per window
    keyGenerator: (c) => c.req.header("x-forwarded-for") ?? "", // Use IP address as key
    message: {
      message: "Too many requests, please try again later",
    },
  }),
);

export type SuccessResponse<T = undefined> = T extends undefined
  ? { success: true; message: string }
  : { success: true; message: string; data: T };

// TODO: refactor this routing
export const appRoutes = app.basePath("/api").route("/", apiRoutes);

// error handling for http errors
app.onError((err, c) => {
  if (err instanceof HTTPException) {
    console.log("Error caause:", err.cause);
    const errorResponse =
      err.res ??
      c.json<ErrorResponse>(
        {
          success: false,
          error: err.message,
          isFormError:
            err.cause && typeof err.cause === "object" && "form" in err.cause
              ? err.cause.form === true
              : false,
        },
        err.status,
      );

    // Return the error response generated by HTTPException
    return errorResponse;
  }

  // For other types of errors, return a generic error response
  return c.json<ErrorResponse>(
    {
      success: false,
      error:
        process.env.NODE_ENV === "production"
          ? "Interal Server Error"
          : (err.stack ?? err.message),
    },
    500,
  );
});

app.get("/", (c) => {
  return c.json({ status: "ok" });
});

app.get("/health", async (c) => {
  try {
    await prisma.$queryRaw`SELECT 1`;
    return c.json({ status: "ok", database: "connected" });
  } catch (error) {
    return c.json(
      { status: "error", database: "disconnected", error: String(error) },
      503,
    );
  }
});

export default {
  port: Number(process.env.PORT) || 3000,
  hostname: "0.0.0.0",
  fetch: app.fetch,
};
